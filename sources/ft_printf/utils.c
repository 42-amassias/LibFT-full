void	putnchar(
			char c,
			int count)
{
	while (count--)
		ft_putchar_fd(c, 1);
}

int	max(
		int a,
		int b)
{
	if (a > b)
		return (a);
	return (b);
}

int	min(
		int a,
		int b)
{
	if (a < b)
		return (a);
	return (b);
}
